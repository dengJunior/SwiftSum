// MARK: - 将 protocol 的方法声明为 mutating

/**
 1. Swift 的 protocol 不仅可以被 class 类型实现，也适用于 struct 和 enum。
 2. mutating 关键字修饰方法是为了能在该方法中修改 struct 或是 enum 的变量，所以如果你没在接口方法里写 mutating 的话，别人如果用 struct 或者 enum 来实现这个接口的话，就不能在方法里改变自己的变量了。
